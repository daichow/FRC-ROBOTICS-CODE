package javaSolutions;

public class Tutorial6_WhileLoop {
	
	public static void main(String[] args) {
		int i = 1; // integer variable that is initialized to 1
		
		while(i <= 10){ // loops while  the value of i is less than or equal to 10
			System.out.println(i); // prints the value of i
			i++; // increments i by 1
		}
	}
}