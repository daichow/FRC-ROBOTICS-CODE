package javaSolutions;

public class Tutorial4_LogicAndBooooleans {
	
	public static void main(String[] args) {
		boolean variableName = true; // creates boolean variable called variableName and initializes it to true
		
		System.out.println(variableName); // prints to console the value of variableName
		
		System.out.println(3 != 2); // prints to console the value of the comparison operator
		// This results in a true since 3 is not equal to 2
		
		System.out.println(true && false); // prints to console the value of the logic operator
		// This results in a false since the left and right sides of the logic operator are not true
	}
}