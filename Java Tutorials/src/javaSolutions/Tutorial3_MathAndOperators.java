package javaSolutions;

public class Tutorial3_MathAndOperators {

	public static void main(String[] args) {
		int num = 0; // creating num variable as type integer and initializing it to 1
		
		System.out.println(3 + 3); // printing out the value of the expression 3 + 3
		
		num += 5; // incrementing num by 5
		
		System.out.println(num); // printing out the value of the variable num
	}
}