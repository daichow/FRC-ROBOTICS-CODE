package javaSolutions;

public class Tutorial7_ForLoop {
	
	public static void main(String[] args) {
		// you can create the variable inside like here rather than outside of the for
		for(int i = 42; i <= 230; i+=2){ // loops from 42 to 230 and increments 2
			System.out.println(i); // prints increment variable
		}
	}
}